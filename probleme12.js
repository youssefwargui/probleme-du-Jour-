// 1 - Écrire une fonction qui prend un tableau de chaînes et retourne un tableau contenant la longueur de chaque chaîne.
function each(tab, func) {
    for (var i = 0; i < tab.length; i++) {
        return func[tab[i]]

    }
}

function longueur(ch) {
    var t = []
    each(tab, func())
    t.push(ch.length)

    return t
}

